/*
	循环语句：
		初始化:做一些初始操作
		条件判断:让我们知道要做多少次
		循环体:就是要做的事情
		控制条件变化:通过控制条件，让我们在合适的时候结束
*/
class ForDemo {
	public static void main(String[] args) {
		//在控制台输出一次"HelloWorld"
		System.out.println("HelloWorld");

		//在控制台输出十次"HelloWorld"
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");
		System.out.println("HelloWorld");

		//在控制台输出一万次"HelloWorld"
	}
}